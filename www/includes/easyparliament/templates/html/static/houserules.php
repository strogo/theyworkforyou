            <h1>House Rules</h1>
<p>As a registered user, you may add annotations to debate pages on TheyWorkForYou &mdash; now that’s power.</p>

<p>But as we all know, with great power comes great responsibility. In order to keep TheyWorkForYou useful and relevant to everyone, there are a few rules. They are important.</p>

<p>Stick to these and you can’t go wrong. Transgress once, and we’ll send you a warning. Do it again and you’re banned, and that’s as fair as we can make it.</p>
            
            <h2>Annotations should be</h2>
            
            <ul><li><b>Relevant to the debate you are commenting on</b>: that’s <em>completely</em> relevant.</li>
                <li><b>Factual</b>: Check your sources. Citing them is good.</li>
                <li><b>Non-partisan</b>, ie not showing a political bias</li>
                <li><b>Under your own name</b> where possible</li>
                <li><b>Genuinely useful for other readers</b>: and if you can’t say ‘yes’ to this one, that’s a pretty good sign that your comment shouldn’t be added.</li></ul>

            <h2>You might like to</h2>

            <ul><li><b>Provide a link to a webpage</b> &mdash; but that page must also be relevant, factual, useful, and non-partisan.   </li></ul>    
  
            <h2>Things to avoid</h2>
   
            <ul><li><b>Don't go off-topic</b>. You’re commenting on the debate, nothing else.</li>
                <li><b>We don't want your opinions or anecdotes</b>. There are plenty of other forums for those: try your local newspaper, for example.</li>
                <li><b>Avoid bad or upsetting language</b>. All types of people use TheyWorkForYou. Don’t write anything you wouldn’t want your own grandmother or child to read — because they (or someone just like them) might.</li>
                <li><b>We don't tolerate attacks on other users</b>. We are kind to one another here.</li>
                <li><b>Same goes for attacks or disparagement of any section of society</b>. See above.</li>
                <li><b>Don't spam our readers</b>: and we count anything that could be described as an advertisement as spam. Because it is.</li></ul>

            <h2>If you see an annotation which does not adhere to these rules</h2>

            <b>You can report it</b>. You’ll see a small link marked ‘<em>report this annotation</em>’ at the top of the comment, near the timestamp.

We’ll take a look at the annotation and take any necessary steps.

            <b>Thank you for helping us keep TheyWorkForYou helpful, friendly and safe.</b>   
           
